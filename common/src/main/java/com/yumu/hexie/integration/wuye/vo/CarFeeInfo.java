package com.yumu.hexie.integration.wuye.vo;

public class CarFeeInfo {

	private String car_count;
	private String car_no;
	private String car_in_date;
	private String car_out_date;
	private String fee_price;
	private String temp_park_fee_water_id;
	
	public String getCar_count() {
		return car_count;
	}
	public void setCar_count(String car_count) {
		this.car_count = car_count;
	}
	public String getCar_no() {
		return car_no;
	}
	public void setCar_no(String car_no) {
		this.car_no = car_no;
	}
	public String getCar_in_date() {
		return car_in_date;
	}
	public void setCar_in_date(String car_in_date) {
		this.car_in_date = car_in_date;
	}
	public String getCar_out_date() {
		return car_out_date;
	}
	public void setCar_out_date(String car_out_date) {
		this.car_out_date = car_out_date;
	}
	public String getFee_price() {
		return fee_price;
	}
	public void setFee_price(String fee_price) {
		this.fee_price = fee_price;
	}
	public String getTemp_park_fee_water_id() {
		return temp_park_fee_water_id;
	}
	public void setTemp_park_fee_water_id(String temp_park_fee_water_id) {
		this.temp_park_fee_water_id = temp_park_fee_water_id;
	}
}
